public class ComparaIdade {
    public static void main(String[] args) {
        int idade1 = 25;
        int idade2 = 30;

        System.out.println("Idade 1: " + idade1 + " anos.");
        System.out.println("Idade 2: " + idade2 + " anos.");
        int diferenca = idade2 - idade1;
        System.out.println("A diferença de idade é: " + diferenca + " anos.");
     }
    }
