nome = "Paulo Cesar Freitas"

print(nome[0:5])  # Exibe os primeiros 5 caracteres
print(nome[6:11])  # Exibe os caracteres do índice 6 ao 10
print(nome[6:])  # Exibe do índice 6 até o final
print(nome[-6:])  # Exibe os últimos 6 caracteres
print(nome[0:16:2])  # Exibe o caractere no índice 5, com passo 2 (não exibe nada pois não há segundo caractere)
print(nome[:])  # Exibe toda a string
print(nome[::-1])  # Exibe a string invertida